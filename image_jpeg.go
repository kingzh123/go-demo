//base64 解码 保存成文件
func writeFileByBase64(name string, base64info string){
	//创建文件
	file, err := os.Create(name)
	if err != nil {
		panic(err)
	}
	//解码base64
	unbase64info := make([]byte, strings.Count(base64info, "")-1)
	_, err = base64.StdEncoding.Decode(unbase64info, []byte(base64info))
	if err != nil {
		panic(err)
	}
	//base64写入到文件
	_, err = file.Write(unbase64info)
	if err != nil {
		panic(err)
	}
	file.Close()
}

//图像合成
func imageComposite() {
	//背景图
	backImage, err := os.Open("./img/back.jpg")
	if err != nil {
		panic(err)
	}
	bImage, err := jpeg.Decode(backImage)
	if err != nil {
		panic(err)
	}
	defer backImage.Close()
	//水印图片
	waterImage, err := os.Open("./img/water.png")
	if err != nil {
		panic(err)
	}
	wImage, err := png.Decode(waterImage)
	if err != nil {
		panic(err)
	}
	defer waterImage.Close()
	//图片偏移量
	offset := image.Pt(200, 0)
	//图片的画布大小
	b := bImage.Bounds()
	//创建NewRGBA编码器
	m := image.NewRGBA(b)
	//画背景图 draw.Src: 源图像透过遮罩后，覆盖在目标图像上（官方解释）图片不漏空
	draw.Draw(m, b, bImage, image.ZP, draw.Src)
	//画水印图 draw.Over: 源图像透过遮罩后，替换掉目标图像 （官方解释）png图片漏空
	draw.Draw(m, wImage.Bounds().Add(offset), wImage, image.ZP, draw.Over)
	//创建图片
	newImage, _ := os.Create("./img/newImage.jpg")
	//图片编码写入文件
	jpeg.Encode(newImage, m, &jpeg.Options{jpeg.DefaultQuality})
	defer newImage.Close()
}

//解码JPEG
func decoderJpeg(data string){
	reader, err := os.Open("img/test.jpg")
	if err != nil {
		panic(err)
	}
	defer reader.Close()
	//reader := base64.NewDecoder(base64.StdEncoding, strings.NewReader(data))
	img, _, err := image.Decode(reader)
	if err != nil {
		panic(err)
	}
	bounds := img.Bounds()
	// 计算m的红色，绿色，蓝色和alpha分量的16-bin直方图。
	//
	// 图像的边界不一定从（0,0）开始，因此两个循环开始
	// 在bounds.Min.Y和bounds.Min.X。 首先循环Y和X秒更多
	// 可能导致比X first和Y second更好的内存访问模式。
	var histogram [16][4]int
	for y := bounds.Min.Y; y < bounds.Max.Y; y++ {
		for x := bounds.Min.X; x < bounds.Max.X; x++ {
			r, g, b, a := img.At(x, y).RGBA()
			// 颜色的RGBA方法返回[0,65535]范围内的值。
			// 移位12将其减小到[0,15] .g的范围
			histogram[r>>12][0]++
			histogram[g>>12][1]++
			histogram[b>>12][2]++
			histogram[a>>12][3]++
		}
	}
	// 打印结果。
	fmt.Printf("%-14s %6s %6s %6s %6s\n", "bin", "red", "green", "blue", "alpha")
	for i, x := range histogram {
		fmt.Printf("0x%04x-0x%04x: %6d %6d %6d %6d\n", i<<12, (i+1)<<12-1, x[0], x[1], x[2], x[3])
	}
}

